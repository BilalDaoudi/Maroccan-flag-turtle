T.speed(1)
T.up()
T.setheading(90)
T.bk(100)
T.down()
T.begin_fill()
T.fillcolor('red')
T.fd(240)
T.setheading(0)
T.fd(200)
T.setheading(-90)
T.fd(150)
T.setheading(180)
T.fd(200)
T.end_fill()

T.up()
T.goto(60,20)
T.down()

T.color('Green')
